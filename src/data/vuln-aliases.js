// ── KB RESOLVER ─────────────────────────────────────────────────────────────
const getKB = (t) => {
  if (VULN_KB[t]) return VULN_KB[t];
  if (VULN_ALIASES[t]) return VULN_KB[VULN_ALIASES[t]] || { ...VULN_KB["default"], label: t, _isDefault: true };
  const tLow = t.toLowerCase();
  for (const [alias, canonical] of Object.entries(VULN_ALIASES)) {
    if (tLow.includes(alias.toLowerCase())) return VULN_KB[canonical] || { ...VULN_KB["default"], label: t, _isDefault: true };
  }
  return { ...VULN_KB["default"], label: t, _isDefault: true };
};
